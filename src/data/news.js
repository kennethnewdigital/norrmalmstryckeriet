function dateLabel(iso) {
  const [y, m, d] = iso.split('-').map(Number);
  return `${d} ${monthsSv[m - 1]} ${y}`;
}
function dotDate(iso) { return iso.replace(/-/g, '.'); }
function readMin(post) {
  const words = (post.excerpt + ' ' + post.body.map((b) => b.v).join(' ')).split(/\s+/).length;
  return Math.max(1, Math.round(words / 200));
}

